s = "come stai?"
print("stringa: ", s)
print("creo un offset della stringa: ", s[3:-1])
#  stamperò dal carattere di posizione 3 (ricorda che parte da 0) al carattere -1

s2 = "mi_piacciono_i_treni"
print("creo offset della stringa 2: ", s2[0:3])
# ricorda che quando metto 3 intendo l'indice di posizione 3. di conseguenza 3 sarebbe l'indice 2.
# 0:m - 1:i - 2:_ - 3:p - 4:i - ....
# quindi 3 sarebbe l'indice di posizione 2 quindi _

# quando si usa l'offset (o un sottinsieme) la sintassi è stringa/lista/quel che vuoi [start:stop]
